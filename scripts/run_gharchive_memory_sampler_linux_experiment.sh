#!/bin/zsh
set -euo pipefail

if [[ $# -lt 4 ]]; then
  echo "usage: $0 <report_path> <server_log_path> <ingester_log_path> <sampler_path>" >&2
  exit 1
fi

REPORT_PATH="$1"
SERVER_LOG_PATH="$2"
INGESTER_LOG_PATH="$3"
SAMPLER_PATH="$4"

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
IMAGE="${BUN_DOCKER_IMAGE:-oven/bun:1.3.6}"
CONTAINER_NAME="${BUN_DOCKER_CONTAINER_NAME:-streams-memory-sampler-linux}"
WORKDIR_IN_CONTAINER="${REPO_ROOT}"
PORT="${PORT:-8787}"
DS_ROOT="${DS_ROOT:-/tmp/lib/prisma-streams-sampler-linux}"

: "${DURABLE_STREAMS_R2_ACCOUNT_ID:?set DURABLE_STREAMS_R2_ACCOUNT_ID}"
: "${DURABLE_STREAMS_R2_BUCKET:?set DURABLE_STREAMS_R2_BUCKET}"
: "${DURABLE_STREAMS_R2_ACCESS_KEY_ID:?set DURABLE_STREAMS_R2_ACCESS_KEY_ID}"
: "${DURABLE_STREAMS_R2_SECRET_ACCESS_KEY:?set DURABLE_STREAMS_R2_SECRET_ACCESS_KEY}"
: "${DS_LOCAL_BACKLOG_MAX_BYTES:?set DS_LOCAL_BACKLOG_MAX_BYTES}"
: "${DS_SEGMENT_CACHE_MAX_BYTES:?set DS_SEGMENT_CACHE_MAX_BYTES}"
: "${DS_INDEX_RUN_CACHE_MAX_BYTES:?set DS_INDEX_RUN_CACHE_MAX_BYTES}"

docker rm -f "${CONTAINER_NAME}" >/dev/null 2>&1 || true

docker run --rm --name "${CONTAINER_NAME}" \
  -v "${REPO_ROOT}:${WORKDIR_IN_CONTAINER}" \
  -w "${WORKDIR_IN_CONTAINER}" \
  -e REPORT_PATH="${REPORT_PATH}" \
  -e SERVER_LOG_PATH="${SERVER_LOG_PATH}" \
  -e INGESTER_LOG_PATH="${INGESTER_LOG_PATH}" \
  -e SAMPLER_PATH="${SAMPLER_PATH}" \
  -e DS_ROOT="${DS_ROOT}" \
  -e PORT="${PORT}" \
  -e DURABLE_STREAMS_R2_ACCOUNT_ID="${DURABLE_STREAMS_R2_ACCOUNT_ID}" \
  -e DURABLE_STREAMS_R2_BUCKET="${DURABLE_STREAMS_R2_BUCKET}" \
  -e DURABLE_STREAMS_R2_ACCESS_KEY_ID="${DURABLE_STREAMS_R2_ACCESS_KEY_ID}" \
  -e DURABLE_STREAMS_R2_SECRET_ACCESS_KEY="${DURABLE_STREAMS_R2_SECRET_ACCESS_KEY}" \
  -e DS_LOCAL_BACKLOG_MAX_BYTES="${DS_LOCAL_BACKLOG_MAX_BYTES}" \
  -e DS_SEGMENT_CACHE_MAX_BYTES="${DS_SEGMENT_CACHE_MAX_BYTES}" \
  -e DS_INDEX_RUN_CACHE_MAX_BYTES="${DS_INDEX_RUN_CACHE_MAX_BYTES}" \
  -e BASE_URL="${BASE_URL:-http://127.0.0.1:${PORT}}" \
  -e STREAM_PREFIX="${STREAM_PREFIX:-gharchive-sampler-linux}" \
  -e AUTO_TUNE_MB="${AUTO_TUNE_MB:-4096}" \
  -e INGEST_SAMPLES="${INGEST_SAMPLES:-6}" \
  -e RECOVERY_SAMPLES="${RECOVERY_SAMPLES:-6}" \
  -e SAMPLE_INTERVAL_SECONDS="${SAMPLE_INTERVAL_SECONDS:-300}" \
  -e SAMPLER_INTERVAL_MS="${SAMPLER_INTERVAL_MS:-1000}" \
  -e GHARCHIVE_NOINDEX="${GHARCHIVE_NOINDEX:-0}" \
  -e GHARCHIVE_ONLYINDEX="${GHARCHIVE_ONLYINDEX:-}" \
  -e DEBUG_PROGRESS_INTERVAL_MS="${DEBUG_PROGRESS_INTERVAL_MS:-5000}" \
  -e TARGET_UPLOADED_SEGMENTS="${TARGET_UPLOADED_SEGMENTS:-0}" \
  -e TARGET_LOGICAL_SIZE_BYTES="${TARGET_LOGICAL_SIZE_BYTES:-0}" \
  -e MAX_INGEST_SECONDS="${MAX_INGEST_SECONDS:-0}" \
  "${IMAGE}" sh -lc '
    set -euo pipefail
    export DEBIAN_FRONTEND=noninteractive
    apt-get update >/tmp/streams-linux-apt-update.log
    apt-get install -y --no-install-recommends zsh curl sqlite3 procps >/tmp/streams-linux-apt-install.log
    zsh scripts/run_gharchive_memory_sampler_experiment.sh "$REPORT_PATH" "$SERVER_LOG_PATH" "$INGESTER_LOG_PATH" "$SAMPLER_PATH"
  '
